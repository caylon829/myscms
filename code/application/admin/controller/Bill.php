    /**
     * 查看
     */
    public function index()
    {
        //当前是否为关联查询
        $this->relationSearch = true;
        //设置过滤方法
        $this->request->filter(['strip_tags']);
        if ($this->request->isAjax())
        {
            //如果发送的来源是Selectpage，则转发到Selectpage
            if ($this->request->request('keyField'))
            {
                return $this->selectpage();
            }
            list($where, $sort, $order, $offset, $limit) = $this->buildparams();
            $this->relationSearch = true;
            $total = $this->model
                    ->with(['supplier','contract'])
                    ->where($where)
                    ->order($sort, $order)
                    ->count();

            $list = $this->model
                    ->with(['supplier','contract'])
                    ->where($where)
                    ->order($sort, $order)
                    ->limit($offset, $limit)
                    ->select();

            $list = collection($list)->toArray();
            $result = array("total" => $total, "rows" => $list);

            return json($result);
        }
        return $this->view->fetch();
    }

    /**
     * 添加
     */
    public function add()
    {
        if ($this->request->isPost()) {
            $params = $this->request->post("row/a");
            $contract_price=Db::table('fa_contract')->field('price')->where('id='.$params['contract_id'])->find();
            if ($params) {
                if($contract_price['price']<$params['price'])
                    $this->error('账单金额不能大于所关联合同的金额，请修改价格！');
                if($params['period_startdate']>$params['period_enddate'])
                    $this->error('计费终止日期不能早于计费起始日期,请修改计费周期!');
                $params = $this->preExcludeFields($params);
                $arrperiod_start=explode('-',$params['period_startdate']);
                $arrperiod_end=explode('-',$params['period_enddate']);
                if($arrperiod_start[1]==$arrperiod_end[1]){
                    $params['period']=$arrperiod_start[0].'-'.$arrperiod_start[1];
                }else{
                    $days = date('t', strtotime($params['period_startdate']));
                    $tempresult=$days-$arrperiod_start[2];
                    if($tempresult>$arrperiod_end[2]){
                        $params['period']=$arrperiod_start[0].'-'.$arrperiod_start[1];
                    }else{
                        $params['period']=$arrperiod_end[0].'-'.$arrperiod_end[1];
                    }
                }
                if ($this->dataLimit && $this->dataLimitFieldAutoFill) {
                    $params[$this->dataLimitField] = $this->auth->id;
                }
                $result = false;
                Db::startTrans();
                try {
                    //是否采用模型验证
                    if ($this->modelValidate) {
                        $name = str_replace("\\model\\", "\\validate\\", get_class($this->model));
                        $validate = is_bool($this->modelValidate) ? ($this->modelSceneValidate ? $name . '.add' : $name) : $this->modelValidate;
                        $this->model->validateFailException(true)->validate($validate);
                    }
                    $result = $this->model->allowField(true)->save($params);
                    Db::commit();
                } catch (ValidateException $e) {
                    Db::rollback();
                    $this->error($e->getMessage());
                } catch (PDOException $e) {
                    Db::rollback();
                    $this->error($e->getMessage());
                } catch (Exception $e) {
                    Db::rollback();
                    $this->error($e->getMessage());
                }
                if ($result !== false) {
                    $this->success();
                } else {
                    $this->error(__('No rows were inserted'));
                }
            }
            $this->error(__('Parameter %s can not be empty', ''));
        }
        return $this->view->fetch();
    }
